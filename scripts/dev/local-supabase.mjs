import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

const ROOT_DIR = new URL('../../', import.meta.url);
const ROOT_PATH = fileURLToPath(ROOT_DIR);
const textDecoder = new TextDecoder();

function normalizeOutput(value) {
  if (typeof value === 'string') {
    return value.trim();
  }

  if (!value || value.length === 0) {
    return '';
  }

  return textDecoder.decode(value).trim();
}

export function runCommand(command, args, options = {}) {
  const result = spawnSync(command, args, {
    cwd: ROOT_PATH,
    stdio: 'pipe',
    encoding: 'utf8',
    ...options,
  });

  if (result.status !== 0) {
    const stderr = normalizeOutput(result.stderr);
    const stdout = normalizeOutput(result.stdout);
    const detail = stderr || stdout || `Command failed: ${command} ${args.join(' ')}`;
    throw new Error(detail);
  }

  return normalizeOutput(result.stdout);
}

function sleep(milliseconds) {
  return new Promise((resolve) => setTimeout(resolve, milliseconds));
}

function isDockerReady() {
  const result = spawnSync('docker', ['info'], {
    stdio: 'ignore',
  });

  return result.status === 0;
}

export async function ensureDockerReady() {
  if (isDockerReady()) {
    return;
  }

  if (process.platform === 'darwin') {
    spawnSync('open', ['-a', 'Docker'], { stdio: 'ignore' });
  }

  for (let attempt = 0; attempt < 45; attempt += 1) {
    if (isDockerReady()) {
      return;
    }

    await sleep(2_000);
  }

  throw new Error('Docker is not ready. Start Docker Desktop and rerun `pnpm dev:local`.');
}

function parseEnvBlock(content) {
  const entries = {};

  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) {
      continue;
    }

    const separatorIndex = trimmed.indexOf('=');
    if (separatorIndex === -1) {
      continue;
    }

    const key = trimmed.slice(0, separatorIndex).trim();
    const rawValue = trimmed.slice(separatorIndex + 1).trim();
    entries[key] = rawValue.replace(/^"(.*)"$/, '$1');
  }

  return entries;
}

export function readEnvFile(pathname) {
  try {
    const content = readFileSync(new URL(pathname, ROOT_DIR), 'utf8');
    return parseEnvBlock(content);
  } catch {
    return {};
  }
}

export function getSupabaseStatusEnv() {
  const output = runCommand('pnpm', ['exec', 'supabase', 'status', '-o', 'env'], {
    cwd: ROOT_PATH,
  });

  return parseEnvBlock(output);
}

export function writeEnvFile(pathname, entries) {
  const lines = [
    '# Generated by scripts/dev. Safe to edit; rerun local bootstrap to refresh Supabase values.',
    ...Object.entries(entries).map(([key, value]) => `${key}=${value}`),
    '',
  ];

  const filePath = fileURLToPath(new URL(pathname, ROOT_DIR));
  mkdirSync(dirname(filePath), { recursive: true });
  writeFileSync(filePath, `${lines.join('\n')}`, 'utf8');
}

